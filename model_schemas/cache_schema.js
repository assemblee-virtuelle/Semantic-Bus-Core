'use strict';

var mongoose = require('mongoose');

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

var CacheSchema = mongoose.Schema({
  data: Object,
  date: {
    type: Date,
    default: Date.now
  },
  history: [{
    date: {
      type: Date,
      default: Date.now
    },
    data: Object
  }],
});

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

module.exports = CacheSchema;
